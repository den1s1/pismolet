        <p>Сервис не продаёт клиенту базы адресатов, не собирает адреса за клиента, не предоставляет формы подписки адресатов и не подтверждает законность базы клиента.</p>
        <p>Отправка выполняется по поручению клиента. Клиент самостоятельно определяет содержание письма, круг адресатов, основание для обработки email-адресов и допустимость отправки.</p>

        <h2>3. Акцепт соглашения</h2>
        <p>Клиент принимает это соглашение при регистрации, первом входе или ином явном подтверждении в интерфейсе сервиса.</p>
        <p>Продолжение использования сервиса после публикации новой редакции соглашения означает согласие клиента с обновлёнными условиями, если сервис прямо не требует отдельного повторного подтверждения.</p>
        <p>Юридически значимые подтверждения клиента могут фиксироваться в журнале legal evidence: дата и время, IP, user-agent, route экрана, версия текста, hash текста и результат действия.</p>

        <h2>4. Аккаунт клиента</h2>
        <p>Для работы с сервисом клиент указывает ФИО, email, телефон и иные данные, необходимые для регистрации, связи, оплаты, поддержки, безопасности и исполнения договора.</p>
        <p>Клиент обязан указывать достоверные контактные данные и поддерживать их актуальность.</p>
        <p>Клиент отвечает за сохранность доступа к аккаунту и действия, совершённые из его аккаунта.</p>
        <p>Сервис может ограничить доступ к аккаунту при нарушении соглашения, подозрительных действиях, жалобах, обходе ограничений или риске нарушения закона.</p>

        <h2>5. База адресатов</h2>
        <p>Клиент загружает только собственную законную базу email-адресов.</p>
        <p>Клиент подтверждает, что имеет законное основание для обработки загружаемых email-адресов и отправки писем этим адресатам.</p>
        <p>Запрещено использовать купленные, украденные, спарсенные, чужие, скачанные из открытых источников или иным образом незаконно полученные базы.</p>
        <p>Сервис в MVP принимает к обработке по получателям только email-адреса. ФИО получателей, телефоны, должности, названия организаций и иные поля персонализации не должны загружаться и использоваться.</p>
        <p>Если клиент выбирает источник базы или подтверждает декларацию законности базы, эти действия могут фиксироваться в legal evidence.</p>

        <h2>6. Рекламные и информационные письма</h2>
        <p>Клиент самостоятельно определяет тип письма: информационное, рекламное, сервисное/транзакционное или сомнительное.</p>
