        <p>Сервис не подтверждает, что конкретное письмо не является рекламой. При сомнениях сервис может потребовать дополнительное подтверждение или отправить рассылку на проверку.</p>

        <h2>7. Запрещённое использование</h2>
        <p>Запрещено использовать сервис для спама, незаконных рассылок, обхода отписок, фишинга, мошенничества, вредоносных ссылок, сокрытия отправителя, агрессивной рекламы, вводящих в заблуждение обещаний и иных нарушений правил сервиса.</p>
