        <p>Запрещено пытаться обойти лимиты, проверки, модерацию, отписку через Письмолёт или технические ограничения сервиса.</p>

        <h2>8. Проверки, модерация и отказ в отправке</h2>
        <p>Сервис может проверять формат адресов, дубли, отписки через Письмолёт, технические ошибки, признаки риска, содержание письма, ссылки, историю аккаунта и жалобы.</p>
        <p>Проверка сервиса не является юридическим заключением и не подтверждает законность базы клиента.</p>
        <p>Сервис может отказать в отправке, отправить рассылку на модерацию, ограничить лимиты или заблокировать аккаунт при нарушении правил, жалобах, признаках незаконной базы или рискованном содержании.</p>

        <h2>9. Отписка через Письмолёт</h2>
        <p>Каждое письмо должно содержать служебный блок и возможность отказаться от дальнейшего получения писем через сервис.</p>
        <p>Если адресат отказался от писем через Письмолёт, этот адрес больше не должен получать рассылки от любых клиентов сервиса.</p>
        <p>Адреса, исключённые из-за отписки через Письмолёт, не отправляются и не должны оплачиваться как принятые к отправке.</p>

