        <p>Сервис может технически принимать ответы адресатов, события доставки, отписки, ошибки и жалобы, чтобы переслать их клиенту, обработать ограничение отправки или зафиксировать юридически значимое событие.</p>
        <p>При жалобе адресата или вопросе о законности базы сервис может запросить у клиента подтверждение источника адреса, даты и способа получения согласия, договора, заявки, формы регистрации или иного основания.</p>
        <p>Если подтверждения не предоставлены или недостаточны, сервис может ограничить рассылку, снизить лимиты, отправить новые рассылки на модерацию или заблокировать аккаунт.</p>

        <h2>11. Оплата и запуск рассылки</h2>
        <p>Перед оплатой клиент видит расчёт стоимости и подтверждает запуск рассылки.</p>
        <p>Оплачиваются только адреса, принятые к отправке после проверки. Дубли, ошибочные адреса, адреса из отписки через Письмолёт и иные исключённые адреса не должны учитываться как оплачиваемые.</p>
        <p>Оплата не гарантирует отправку рассылки, если после оплаты или дополнительной проверки выявлены нарушение правил, высокий риск, жалобы, техническая ошибка или запрет к отправке.</p>
        <p>Правила оплаты, запуска и возвратов публикуются отдельным документом.</p>

        <h2>12. Ограничения ответственности</h2>
        <p>Сервис предоставляет технический инструмент и не несёт ответственность за законность базы клиента, содержание письма, наличие согласий адресатов и последствия действий клиента.</p>
        <p>Сервис не гарантирует доставку каждого письма, открытие, переход, ответ адресата или отсутствие попадания письма в папку «Спам».</p>
        <p>Клиент самостоятельно отвечает за соблюдение законодательства о персональных данных, рекламе, связи, защите потребителей и иных применимых требований.</p>

        <h2>13. Изменение условий</h2>
        <p>Сервис может обновлять это соглашение, правила рассылок, тарифы, правила оплаты, политики обработки данных и иные документы.</p>
        <p>Для юридически значимых подтверждений сервис хранит версию текста, действовавшую на момент подтверждения.</p>
        <p>Если изменение существенно влияет на права или обязанности клиента, сервис может запросить повторное подтверждение в интерфейсе.</p>

        <h2>14. Связанные документы</h2>
        <p>Это соглашение применяется вместе с другими документами сервиса: политикой обработки персональных данных, правилами рассылок, антиспам-политикой, политикой запрещённого контента, правилами отписки через сервис, правилами оплаты и возвратов, поручением на обработку данных адресатов и иными документами, опубликованными в разделе юридических документов.</p>
        """;

